package logica.Sentencias_De_Decision;

public class cicloFor {
    public static void main(String[] args) {
        for (var i =1; i<5; i++){
            System.out.println(i);
        }

        //otro ejercio
        System.out.println("--Ciclo For--");
        for(var letra = 1; letra <= 25; letra++ ){
            System.out.println(letra);
        }

    }
}
